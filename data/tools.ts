export const popularTools = tools.filter((tool) => tool.available);

export function getToolByHref(href: string) {
  return tools.find((tool) => tool.href === href);
}

export function getToolsByCategory(category: ToolCategory) {
  return tools.filter((tool) => tool.category === category);
}

export function getRelatedTools(href: string, limit = 6) {
  const tool = getToolByHref(href);
  if (!tool) {
    return [];
  }

  const relatedByTitle = tool.related
    .map((title) => tools.find((candidate) => candidate.title === title))
    .filter((candidate): candidate is ToolCard => Boolean(candidate));

  const categoryFallback = tools.filter(
    (candidate) =>
      candidate.href !== href &&
      candidate.category === tool.category &&
      !relatedByTitle.some((related) => related.href === candidate.href),
  );

  return [...relatedByTitle, ...categoryFallback].slice(0, limit);
}
